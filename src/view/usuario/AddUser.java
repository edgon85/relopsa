            PreparedStatement statement = conexion.prepareStatement(sql);
            
            statement.setString(1, this.txtNameUser.getText());
            statement.setString(2, this.txtNickUser.getText());
            statement.setString(3, this.txtPassUser.getText());
            statement.setString(4, this.jcboxTypeUser.getSelectedItem().toString());
        
            statement.executeUpdate();
            JOptionPane.showMessageDialog(null, "Insercion exitosa!");
            
            this.dispose();
            
            if (Usuarios.isLightweightComponent(this)) {
                
            }
            
      
